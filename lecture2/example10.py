a = 10  # Binary: 1010
b = 4   # Binary: 0100

# AND
print("a & b =", a & b)  # Output: a & b = 0 (Binary: 0000)

# OR
print("a | b =", a | b)  # Output: a | b = 14 (Binary: 1110)

# XOR
print("a ^ b =", a ^ b)  # Output: a ^ b = 14 (Binary: 1110)

# NOT
print("~a =", ~a)  # Output: ~a = -11 (Binary: ...11110101)

# Left shift
print("a << 2 =", a << 2)  # Output: a << 2 = 40 (Binary: 101000)

# Right shift
print("a >> 2 =", a >> 2)  # Output: a >> 2 = 2 (Binary: 0010)
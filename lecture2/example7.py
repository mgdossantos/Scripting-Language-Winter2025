
a = 10
b = 3

# Equal to
print("a == b:", a == b)  # Output: a == b: False

# Not equal to
print("a != b:", a != b)  # Output: a != b: True

# Greater than
print("a > b:", a > b)  # Output: a > b: True

# Less than
print("a < b:", a < b)  # Output: a < b: False

# Greater than or equal to
print("a >= b:", a >= b)  # Output: a >= b: True

# Less than or equal to
print("a <= b:", a <= b)  # Output: a <= b: False
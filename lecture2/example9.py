a = 10

# Assign
a = 5
print("a =", a)  # Output: a = 5

# Add and assign
a += 3
print("a += 3:", a)  # Output: a += 3: 8

# Subtract and assign
a -= 2
print("a -= 2:", a)  # Output: a -= 2: 6

# Multiply and assign
a *= 4
print("a *= 4:", a)  # Output: a *= 4: 24

# Divide and assign
a /= 3
print("a /= 3:", a)  # Output: a /= 3: 8.0

# Floor divide and assign
a //= 2
print("a //= 2:", a)  # Output: a //= 2: 4.0

# Modulus and assign
a %= 3
print("a %= 3:", a)  # Output: a %= 3: 1.0

# Exponentiation and assign
a **= 3
print("a **= 3:", a)  # Output: a **= 3: 1.0